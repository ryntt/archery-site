                <h2>Arrow</h2>
                    <p>
                        The arrow is one of the two most important pieces of equipment in archery, the other being the bow. The arrow 
                        is the projectile that an archer nocks and shoots with a bow. Traditional arrows were made of basic materials such
                        as wood and bone, but in modern times, most arrows are made of strong metals and materials such as aluminum or carbon fiber.
                    </p>
                <h2>Bow</h2>
                    <p>
                        The bow is one of the two most important pieces of equipment in archery, the other being the arrow. The bow 
                        is required to shoot an arrow, and it is typically made of either wood, metal, or a combination of both. Most bows
                        also have modifications that allow archers to equip accessories such as sights and stablizers. There are three types of bows:
                    </p>
                        <ul>
                            <li>
                                Recurve bow: this is one of the two most common types of bows used today. When the string of a recurve bow is pulled back, 
                                a great deal of stress is placed on the limbs (ends), and the limbs are pulled toward the archer. Without its string, a recurve bow resembles the number three. 
                                Recurve bows are the only type allowed in certain competitions such as the Olympics.
                            </li>
                            <br>
                            <li>
                                Compound bow: this is one of the two most common types of bows used today. A compound bow has various pulleys that help 
                                an archer pull, aim, and shoot with more power and less energy. For this reason, compound bows are highly popular among hunters. 
                                However, it is also partly why they are not allowed in the Olympics. 
                            </li>
                            <br>
                            <li>
                                Straight bow (longbow): this is a less common type of bow used today. It is the most primitive type of bow 
                                and shares some similarities with the recurve bow. With a string attached, a straight bow resembles the long half of an oval. 
                                Straight bows have less power than other bows, but they are relatively simple to make because of their simple designs. 
                            </li>
                        </ul>
                <h2>Quiver</h2>
                    <p>
                        The quiver is a container, akin to a large pouch, that holds an archer's arrows. Quivers are typically worn either on an archer's 
                        back or on their hip. There are two types of quivers worn on the hip:
                    </p>
                        <ul>
                            <li>
                                Hip quivers (sometimes known as target quivers): arrows placed in these quivers lean forward, with the fletching facing
                                forward and the points facing downward towards the archer's back. With these quivers, it is easy to count and pick arrows, 
                                but they could be inconvenient if they bump into others in an archer's vicinity or get caught in something.
                            </li>
                            <br>
                            <li>
                                Field quivers: arrows placed in these quivers lean backward, with the fletching facing backward and the points facing downward 
                                toward the archer's chest. With these quivers (and back quivers), it is difficult to count and pick arrows, so archers would need 
                                to keep track of their arrows somehow. However, they are more convenient to take and walk around with and don't get caught on things 
                                as often.
                            </li>
                        </ul>
