            <h1>Auxiliary Equipment</h1>
                <h2>Arm Guard</h2>
                    <p>
                        Arm guards protect archers from getting their forearms struck by the bowstring, also known as string 
                        slap. This is very painful, and could possibly (though rarely) lead to serious complications. Some arm 
                        guards prioritize functionality over beauty, while some have intricate designs meant as displays 
                        of power and wealth. 
                    </p>
                <h2>Chest Guard</h2>
                    <p>
                        Chest guards protect archers from breast injuries and to keep clothes in place while they shoot. Most female archers 
                        wear chest guards, though some male archers also wear them. 
                    </p>
                <h2>Finger Tab</h2>
                    <p>
                        Finger tabs protect archers' fingers from bowstring injuries. The force generated from releasing the bowstring could 
                        cause some issues, and the bowstring itself could cut an archer's fingers with enough pressure. 
                    </p>
                <h2>Sight</h2>
                    <p>
                        Some archers, especially those who practice hunting, equip sights on their bows for better aiming. Most sights 
                        have a series of horizontal pins inside, though advanced sights have more sophisticated capabilities such as 
                        a digital display. 
                    </p>
                <h2>Stabilizer</h2>
                    <p>
                        Stabilizers are weighted rods archers attach to a bow to increase stability and make aiming easier. Without stabilizers, 
                        a bow would move excessively while an archer shoots, reducing precision and accuracy and impeding performance.
                    </p>
